package cc.lelecat.tag.lable;

/**
 * 标签
 * 储存标签的名称和标签的图标
 * Created by ACat on 2016/11/11.
 * ACat i lele.
 */
public class Lable {
	protected String name;
	protected String icon;
	protected String url;

	public Lable(String name) {
		this.name = name;
	}

	public Lable(String name, String url) {
		this.name = name;
		this.url = url;
	}

	public Lable(String name, String url, String icon) {
		this.name = name;
		this.icon = icon;
		this.url = url;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}
}
