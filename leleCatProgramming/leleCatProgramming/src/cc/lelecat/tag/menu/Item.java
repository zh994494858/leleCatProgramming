package cc.lelecat.tag.menu;

import cc.lelecat.tag.lable.Lable;

/**
 * 功能菜单项
 * level: 是否为子菜单
 * 		false 一级菜单
 * 		true 上一个菜单的子菜单
 * lable:	名称
 * remind:	提醒标识的数字
 *
 * Created by ACat on 2016/11/10.
 * ACat i lele.
 */
public class Item extends Lable {
	private int level;
	private int remind;
	private String cmd;

	public Item(String name) {
		super(name);
	}

	public Item(String name, String url) {
		super(name, url);
	}

	public Item(String name, String url, String icon) {
		super(name, url, icon);
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getRemind() {
		return remind;
	}

	public void setRemind(int remind) {
		this.remind = remind;
	}

	public String getCmd() {
		return cmd;
	}

	public void setCmd(String cmd) {
		this.cmd = cmd;
	}
}
